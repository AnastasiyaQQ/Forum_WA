using Forum.Dtos.Auth;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
// Тип элемента - Интерфейс Сервиса 
// Назначение типа: Интерфейсы определяют набор методов, которые должен реализовать класс сервиса.
//  Назначение элемента - Интерфейс Сервиса Аутентификации 
// Определяет операции, связанные с входом и регистрацией пользователей.

namespace Forum.Services.Interfaces
{
    //  Метод - LoginAsync 
    // Назначение - Определяет метод для выполнения входа пользователя в систему.
    // Что делает - Реализация этого метода проверит учетные данные пользователя (логин/пароль)
    // и в случае успеха сгенерирует и вернет токен доступа (JWT).
    // Параметры - loginDto - объект с данными для входа (логин и пароль).
    // Возвращает - Task<ActionResult<TokenDto>> - Асинхронная операция, которая вернет ActionResult.
    // В случае успеха, ActionResult будет содержать TokenDto (токен и информация о пользователе).
    // В случае ошибки (неверный пароль/логин), вернет соответствующий ActionResult (например, Unauthorized).

    //  Метод - RegisterAsync 
    // Назначение - Определяет метод для регистрации нового пользователя.
    // Что делает - Реализация этого метода проверит, не занято ли имя пользователя,
    // создаст хеш пароля, сохранит нового пользователя в базе данных с ролью "User"
    // и вернет результат операции.
    // Параметры - registerDto - объект с данными для регистрации (логин и пароль).
    // Возвращает - Task<ActionResult> - Асинхронная операция, которая вернет ActionResult.
    // В случае успеха, вернет Ok или подобный статус.
    // В случае ошибки (имя занято), вернет BadRequest или подобный статус.

    public interface IAuthService
    {
        Task<ActionResult<TokenDto>> LoginAsync(LoginDto loginDto);
        Task<ActionResult> RegisterAsync(RegisterDto registerDto);
    }
}

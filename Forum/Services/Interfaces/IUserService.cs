using System.Security.Claims;
using Forum.Dtos.Users;
// Тип элемента - Интерфейс Сервиса 
// Назначение типа: Интерфейсы определяют набор методов, которые должен реализовать класс сервиса.
//  Назначение элемента - Интерфейс Сервиса Пользователей 
// Определяет вспомогательные методы для работы с данными текущего пользователя и получения информации о пользователях.

namespace Forum.Services.Interfaces
{
    //  Метод - GetCurrentUserId 
    // Назначение - Определяет метод для получения ID текущего аутентифицированного пользователя.
    // Что делает - Реализация извлечет ID пользователя из ClaimsPrincipal (обычно из клейма NameIdentifier).
    // Параметры - userPrincipal - объект ClaimsPrincipal, представляющий текущего пользователя (обычно берется из HttpContext.User).
    // Возвращает - int? - ID пользователя, если он аутентифицирован и ID удалось извлечь, иначе null.

    //  Метод - GetUserDtoByIdAsync 
    // Назначение - Определяет метод для получения информации о пользователе в формате DTO по его ID.
    // Что делает - Реализация найдет пользователя в базе данных по ID и вернет его данные (ID, имя, роль) в виде UserDto.
    // Параметры - userId - ID пользователя, информацию о котором нужно получить.
    // Возвращает - Task<UserDto> - Асинхронная операция, которая вернет UserDto с данными пользователя или null, если пользователь не найден.

    public interface IUserService
    {
        int? GetCurrentUserId(ClaimsPrincipal userPrincipal);

        // Метод для получения пользователя
        Task<UserDto> GetUserDtoByIdAsync(int userId); 
    }
}

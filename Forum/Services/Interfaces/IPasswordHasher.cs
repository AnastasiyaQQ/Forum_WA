// Тип элемента - Интерфейс Сервиса 
// Назначение типа: Интерфейсы определяют набор методов, которые должен реализовать класс сервиса.
// Использование интерфейса позволяет легко заменять реализацию алгоритма хеширования.
// Назначение элемента - Интерфейс Сервиса Хеширования Паролей 
// Определяет основные операции для безопасной работы с паролями - создание хеша и проверка соответствия пароля хешу.
namespace Forum.Services.Interfaces
{
    public interface IPasswordHasher
    {
        //  Метод - HashPassword
        // Назначение - Определяет метод для создания криптографического хеша пароля.
        // Что делает - Реализация преобразует строку пароля в безопасный хеш.
        // Параметры - password - строка пароля, которую нужно хешировать.
        // Возвращает - string - строка, содержащая хеш пароля.

        // Метод - VerifyPassword
        // Назначение - Определяет метод для проверки соответствия введенного пароля сохраненному хешу.
        // Что делает - Реализация сравнит предоставленный пароль с хешем, используя тот же алгоритм и параметры, что и при хешировании.
        // Параметры:
        // hashedPassword - строка с хешем пароля (обычно из базы данных).
        // providedPassword - строка с паролем, введенным пользователем для проверки.
        // Возвращает - bool - true, если предоставленный пароль соответствует хешу, иначе false.
        string HashPassword(string password);
        bool VerifyPassword(string hashedPassword, string providedPassword);
    }
}

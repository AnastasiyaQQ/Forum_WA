using System.Collections.Generic;
//  Тип элемента - DTO (Data Transfer Object) 
// Назначение типа - DTO используются для передачи данных между слоями. 
// Классы, представляющие данные, получаемые от сервера и отправляемые на сервер.

// Этот класс является обобщенным, что позволяет использовать его для пагинации результатов любого типа <T>.
//  Назначение элемента - Результат запроса с пагинацией 
// Структура для возврата данных из API, когда запрашивается список элементов с разбивкой на страницы.
// Содержит как саму страницу данных (Items), так и номера страниц, общее количество.

namespace Forum.Dtos.Common
{
    // T - это параметр типа, который будет заменен конкретным типом DTO при использовании PagedResult
    //  Свойство - Items 
    // Назначение - Содержит список элементов (DTO) для текущей страницы.
    // Тип данных - List<T> (список объектов типа T)

    //  Свойство - PageNumber 
    // Назначение - Номер текущей страницы (начиная с 1).
    // Тип данных - int

    //  Свойство - PageSize 
    // Назначение - Количество элементов, запрошенное на одной странице.
    // Тип данных - int

    //  Свойство - TotalPages 
    // Назначение - Общее количество страниц для данного запроса (рассчитывается на основе TotalCount и PageSize).
    // Тип данных - int

    //  Свойство - TotalCount 
    // Назначение - Общее количество элементов, соответствующих запросу (без учета пагинации).
    // Тип данных - int

    public class PagedResult<T>
    {
        public List<T> Items { get; set; } = new List<T>();
        public int PageNumber { get; set; }
        public int PageSize { get; set; }
        public int TotalPages { get; set; }
        public int TotalCount { get; set; }
    }
}

using System;
//  Тип элемента - DTO (Data Transfer Object) 
// Назначение типа - DTO используются для передачи данных между слоями. 
// Классы, представляющие данные, получаемые от сервера и отправляемые на сервер.

//  Назначение элемента - Информация о комментарии (для списков и отображения) 
// Используется для отображения комментариев в списках (к посту, "Мои комментарии").
// Содержит содержание, автора, дату и ID поста, к которому он относится.

namespace Forum.Dtos.Comments
{
    //  Свойство - Id 
    // Назначение - Уникальный идентификатор комментария.
    // Тип данных - int

    //  Свойство - Content 
    // Назначение - Содержание (текст) комментария.
    // Тип данных - string

    //  Свойство - AuthorName 
    // Назначение - Имя автора комментария.
    // Тип данных - string

    //  Свойство - AuthorId 
    // Назначение - ID автора комментария.
    // Тип данных - int

    //  Свойство - CreatedDate 
    // Назначение - Дата создания комментария.
    // Тип данных - DateTime

    //  Свойство - PostId 
    // Назначение - ID записи, к которой относится данный комментарий.
    // Тип данных - int

    public class CommentDto
    {
        public int Id { get; set; }
        public string Content { get; set; }
        public string AuthorName { get; set; }
        public int AuthorId { get; set; }
        public DateTime CreatedDate { get; set; }
        public int PostId { get; set; }
    }
}
